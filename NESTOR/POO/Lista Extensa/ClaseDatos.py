class Datos:
    def __init__(self,):
        self.lista = []

        def agregar_lista(self, objeto):
            self.lista.append(objeto)

        def imprimir(self):
            for indice, x in enumerate(len(self.lista)):
                print(f"En {indice + 1} esta [{self.lista[x].Nombre}---[{self.lista[x].Control}]")